"""
Algoritmo 36: Ler dois números inteiros e imprimir a soma. Antes do resultado, deverá
aparecer a mensagem: Soma.
"""
numero_inteiro1 = int(input("Digite o primeiro número inteiro: "))
numero_inteiro2 = int(input("Digite o segundo número inteiro: "))

soma = numero_inteiro1 + numero_inteiro2
print(f"Soma: {soma}")