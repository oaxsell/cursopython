"""
Algoritmo 34: Ler um número inteiro e imprimir seu sucessor e seu antecessor.
"""
numero_inteiro = int(input("Digite o primeiro número inteiro: "))
numero_antecessor = numero_inteiro - 1
numero_sucessor = numero_inteiro + 1


print(f"Numero inteiro 1: {numero_inteiro} e Numero Sucessor: {numero_sucessor} e Numero Antecessor: {numero_antecessor}")