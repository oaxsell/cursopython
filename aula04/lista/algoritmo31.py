"""
Algoritmo 31: Criar um algoritmo que imprima a média 
aritmética entre os números 8, 9 e 7.
"""
media = (8 + 9 + 7) / 3
print('A média aritmética entre 8, 9 e 7 é:', media)

"""
Outra forma de fazer:
"""
numeros = [8, 9, 7]
media2 = sum(numeros) / len(numeros)
print('A média aritmética entre 8, 9 e 7 é:', media2)