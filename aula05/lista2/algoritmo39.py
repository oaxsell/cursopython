"""
Algoritmo 39 – Entra com dois números reais e imprimir a média aritmética com a
mensagem “média”
"""
primeiro_numero_real = float(input("Digite o primeiro número real: "))
segundo_numero_real = float(input("Digite o segundo número real: "))
media = (primeiro_numero_real + segundo_numero_real) / 2
print(f"A média entre {primeiro_numero_real} e {segundo_numero_real} é {media}")